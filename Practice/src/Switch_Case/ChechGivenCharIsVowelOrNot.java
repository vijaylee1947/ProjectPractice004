package Switch_Case;

public class ChechGivenCharIsVowelOrNot {

	public static void main(String[] args) {
		char ch='A';
		switch(ch) {
		case 'A':System.out.println("it is an uppercase vowel");
		break;
		case 'E':System.out.println("it is an uppercase vowel");
		break;
		case 'I':System.out.println("it is an uppercase vowel");
		break;
		case 'O':System.out.println("it is an uppercase vowel");
		break;
		case 'U':System.out.println("it is an uppercase vowel");
		break;
		default:System.out.println("it is not an uppercase vowel");
		}
	}

}
